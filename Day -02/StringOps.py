# Multiple strings
first_name="Annamani"
last_name="Kamma"
words="lives in "
Country="Denmark"
# String Concatenation
print(first_name+" "+last_name+","+words+" "+Country+" ")
# Length of a string
print(len(first_name))
# String Lower case
print(Country.lower())
# String Upper Case
print(first_name.upper())

